int	ft_str_is_alpha(char *str)
{
	char *p;

	if (*str == '\0')
		return (1);
	else
	{
		p = str;
		while (*p)
		{
			if (!((*p >= 'a' && *p <= 'z') || (*p >= 'A' && *p <= 'Z')))
				return (0);
			p++;
		}
		return (1);
	}
}
