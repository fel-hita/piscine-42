unsigned int	ft_strlcpy(char *dest, char *src, unsigned int size)
{
	unsigned int	i;
	int				dlen;

	i = 0;
	dlen = 0;
	while (dest[i] != '\0')
	{
		dlen++;
		i++;
	}
	i = 0;
	while (src[i] != '\0' && i < size - 1)
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dlen + size);
}
