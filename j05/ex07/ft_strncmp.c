int	ft_strncmp(char *s1, char *s2, unsigned int n)
{
	unsigned int cmp;

	cmp = 1;
	while ((*s1 == *s2) && ((*s1 != '\0') || (*s2 != '\0')) && cmp < n)
	{
		s1++;
		s2++;
		cmp++;
	}
	return (*s1 - *s2);
}
